package dataTypes;

public class ConcatenateInt {
	/*
	 * Create three int variables having values like : 100, 200, 3400. Add them and
	 * concatenate and generate this output String : "Your Total amount is: 3700".
	 * Solution: int a = 100; int b = 200; int c = 3400; System.out.println(“Your
	 * Total amount is : “ +(a+b+c));
	 */
	public static void main(String[] args) {

		int a = 100;
		int b = 200;
		int c = 3400;

		System.out.println("Total amount is : " + (a + b + c));

	}

}
