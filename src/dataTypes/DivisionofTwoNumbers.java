package dataTypes;

public class DivisionofTwoNumbers {

	/* Write a Java Progtam to print the division of two numbers */
	/* k=50/3 ; Expected Output : 16 */

	public static void main(String[] args) {

		int a = 50;
		int b = 3;
		int c = a / b;
		double k = 50.0 / 3.0;
		System.out.println(c); // 16
		System.out.println(k); // 16.0
	}

}
